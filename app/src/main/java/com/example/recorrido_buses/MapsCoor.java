package com.example.recorrido_buses;

public class MapsCoor {

    private Double lat;
    private Double lon;

    public MapsCoor() {
    }

    public Double getLat() {
        return lat;
    }

    public void setLat(Double lat) {
        this.lat = lat;
    }

    public Double getLon() {
        return lon;
    }

    public void setLon(Double lon) {
        this.lon = lon;
    }
}
